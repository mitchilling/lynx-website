const fs = require('fs');
const path = require('path');

const packagesDir = path.resolve(__dirname, '../.lynx-ui-source/packages');
/** @type {Record<string, string>} */
const enIntros = {};
/** @type {Record<string, string>} */
const zhIntros = {};

if (!packagesDir || !fs.existsSync(packagesDir)) {
  console.warn(
    'lynx-ui source not found. Run pnpm prepare:fetch-lynx-ui first.',
  );
  process.exit(0);
}

const packages = fs.readdirSync(packagesDir);

const extractFirstParagraph = (content) => {
  const lines = content.split('\n');
  let foundHeader = false;

  for (const line of lines) {
    const trimmed = line.trim();
    if (!foundHeader) {
      if (trimmed.startsWith('# ')) {
        foundHeader = true;
      }
      continue;
    }

    if (
      trimmed.length > 0 &&
      !trimmed.startsWith('import ') &&
      !trimmed.startsWith('export ') &&
      !trimmed.startsWith('<') &&
      !trimmed.startsWith('{') &&
      !trimmed.startsWith(':::')
    ) {
      return trimmed;
    }
  }

  return '';
};

const extractIntroFromFile = (filePath) => {
  if (!fs.existsSync(filePath)) {
    return '';
  }

  return extractFirstParagraph(fs.readFileSync(filePath, 'utf8'));
};

for (const pkg of packages) {
  const enIntro = extractIntroFromFile(
    path.join(packagesDir, pkg, 'README.md'),
  );
  if (enIntro) {
    enIntros[pkg] = enIntro;
  }

  const zhIntro =
    extractIntroFromFile(path.join(packagesDir, pkg, 'README_zh.md')) ||
    extractIntroFromFile(path.join(packagesDir, pkg, 'README.zh.md')) ||
    extractIntroFromFile(path.join(packagesDir, pkg, 'docs/README.zh.mdx'));
  if (zhIntro) {
    zhIntros[pkg] = zhIntro;
  }
}

const outputPath = path.resolve(__dirname, '../sharedDocs/lynx-ui-intros.ts');

if (fs.existsSync(outputPath)) fs.unlinkSync(outputPath);

const sortRecord = (record) =>
  Object.fromEntries(
    Object.entries(record).sort(([a], [b]) => a.localeCompare(b)),
  );

const fileContent =
  `// This file is auto-generated by scripts/extract-lynx-ui-intros.js\n` +
  `// Do not edit manually.\n` +
  `\n` +
  `export const lynxUiIntros: Record<string, string> = ${JSON.stringify(
    sortRecord(enIntros),
    null,
    2,
  )};\n` +
  `\n` +
  `export const lynxUiIntrosZh: Record<string, string> = ${JSON.stringify(
    sortRecord(zhIntros),
    null,
    2,
  )};\n`;

fs.writeFileSync(outputPath, fileContent, 'utf8');

console.log(
  'Successfully extracted introductions to sharedDocs/lynx-ui-intros.ts!',
);
